from torch.utils.data import Dataset
from PIL import Image
import torch
import numpy as np

class ASLDynamicDataset(Dataset):

    def __init__(self, root_dir, class_to_idx, seq_len=16, img_size=224):
        self.root_dir = root_dir
        self.class_to_idx = class_to_idx
        self.seq_len = seq_len
        self.img_size = img_size
        self.samples = []
        self.aug_factor = 5

        # Collect all clip frame folders
        for class_name in class_to_idx.keys():

            class_path = os.path.join(root_dir, class_name)

            for item in os.listdir(class_path):
                if item.endswith("_frames"):

                    clip_path = os.path.join(class_path, item)
                    label = class_to_idx[class_name]

                    self.samples.append((clip_path, label))

    def __len__(self):
        return len(self.samples)*self.aug_factor

    def __getitem__(self, idx):

        clip_path, label = self.samples[idx % len(self.samples)]

        frame_files = sorted([
            f for f in os.listdir(clip_path)
            if f.endswith(".jpg")
        ])

        # Uniformly sample 16 frames
        if len(frame_files) >= self.seq_len:
           start = np.random.randint(0, len(frame_files) - self.seq_len + 1)
           indices = np.arange(start, start + self.seq_len)
        else:
           indices = np.linspace(
           0,
           len(frame_files) - 1,
           self.seq_len,
           dtype=int
         )

        frames = []

        for i in indices:
            img_path = os.path.join(clip_path, frame_files[i])


            image = Image.open(img_path).convert("RGB")
            image = np.array(image)

            h, w, _ = image.shape

            # crop upper-center region (focus on hands)
            h, w, _ = image.shape

            top = np.random.randint(0, int(0.3*h))
            left = np.random.randint(0, int(0.3*w))

            image = image[
            top:top+int(0.6*h),
            left:left+int(0.6*w)
            ]
            # resize after cropping
            image = Image.fromarray(image).resize((self.img_size, self.img_size))
            image = np.array(image)
            image = torch.from_numpy(image).permute(2,0,1).float()/255.0

            # ---- brightness augmentation ----
            if random.random() < 0.5:
               image = image * (0.8 + 0.4 * random.random())
               image = torch.clamp(image, 0, 1)

           # ---- horizontal flip ----
            if random.random() < 0.5:
               image = torch.flip(image, dims=[2])

           # ---- normalization ----
            mean = torch.tensor([0.485, 0.456, 0.406]).view(3,1,1)
            std = torch.tensor([0.229, 0.224, 0.225]).view(3,1,1)

            image = (image - mean) / std

            frames.append(image)

        frames = torch.stack(frames)

        return frames, label

