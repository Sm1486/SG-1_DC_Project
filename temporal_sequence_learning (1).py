import torch.nn as nn

class CNN_LSTM_Model(nn.Module):

    def __init__(self, cnn_backbone, num_classes):

        super().__init__()

        self.cnn = cnn_backbone

        # Projection layer to match assignment requirement
        self.feature_proj = nn.Linear(128, 512)

        # Temporal modeling
        self.lstm = nn.LSTM(
            input_size=512,
            hidden_size=512,
            num_layers=2,
            batch_first=True
        )

        # Classification head
        self.classifier = nn.Sequential(
            nn.Linear(512,128),
            nn.ReLU(),
            nn.Linear(128,num_classes)
        )

    def forward(self, x):

     B, T, C, H, W = x.shape

     # Merge batch and time dimension
     x = x.view(B*T, C, H, W)

     # Extract spatial features
     features = self.cnn(x)           # (B*T,128,1,1)

     features = features.view(B*T, 128)

     # Project to 512-dim
     features = self.feature_proj(features)

     # Restore temporal dimension
     features = features.view(B, T, 512)

     # LSTM temporal modeling
     lstm_out, _ = self.lstm(features)

     # Final timestep representation
     final_feature = torch.mean(lstm_out, dim=1)

     output = self.classifier(final_feature)

     return output

